<p>The Closed Kinetic Chain Upper Extremity Stability Test (CKCUES)</p>
<p style="color: rgba(0, 0, 0, 0.701961);">By including a very short and easy test, we get a lot of information on shoulder function. The CKCUES test already showed high reliability in research and recently, Lee et al (2015) (*) demonstrated its validity by testing against hand grip strength and isokinetic internal/external rotation strength. The results showed the CKCUES test had high reliability and high correlations with the results of both hand grip test and isokinetic test, so it is a very useful examination method for clinical use.</p>
<p style="color: rgba(0, 0, 0, 0.701961);">Hand grip is closely related to upper extremity function, facilitates rotator cuff movement and is important for rehabilitation treatment and for shoulder pain. The isokinetic test for internal and external shoulder rotation is an objective and accurate way of measuring shoulder muscle strength and shoulder stability. Lee et al (2015) compared shoulder internal/external peak torque data at angular speeds of 60°/sec and 180°/sec with CKCUES data, and found high correlations for all the results.</p>
